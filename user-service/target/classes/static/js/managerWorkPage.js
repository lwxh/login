async function ajax(url) {
    return new Promise(function (resolve, reject) {
        if((typeof url) != "string") {
            resolve(url);
        } else {
            let ajaxSetting = {
                url: url,
                dataType:'json',
                type:'get',
                success: function (response) {
                    resolve(response);
                },
                error: function () {
                    reject("请求失败");
                }
            }
            $.ajax(ajaxSetting);
        }
    });
}

