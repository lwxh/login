function SplitData(rawData) {
    var categoryData = [];//日期
    var dataOperate = [];//日操作数
    var dataManageFlightNum = [];//管理航班数
    var dailySalesRvenue=[];//日销售收入
    var dailySalesTicketNums=[];//日售票量
    var dataDailySeatsOccupancyRate=[];//日均上座率
    var dataDailyRevenuePerASK=[];//日均座公里收入
    var dataDailyRevenuePerRPK=[]; //日客公里收入数据
    for (var i = 0; i < rawData.length; i++) {
        categoryData.push(rawData[i][0]);
        dataOperate.push(rawData[i][1]);
        dataManageFlightNum.push(rawData[i][2]);
        dailySalesRvenue.push(rawData[i][3]);
        dailySalesTicketNums.push(rawData[i][4]);
        dataDailySeatsOccupancyRate.push(rawData[i][5]);
        dataDailyRevenuePerASK.push(rawData[i][6]);
        dataDailyRevenuePerRPK.push(rawData[i][7]);
    }
    return {
        categoryData: categoryData,
        dataOperate:dataOperate,
        dataManageFlightNum:dataManageFlightNum,
        dailySalesRvenue: dailySalesRvenue,
        dailySalesTicketNums:dailySalesTicketNums,
        dataDailySeatsOccupancyRate:dataDailySeatsOccupancyRate,
        dataDailyRevenuePerASK: dataDailyRevenuePerASK,
        dataDailyRevenuePerRPK:dataDailyRevenuePerRPK,
    };
}



